#define 	STX						0x02
#define 	ETX						0x03
#define 	EOT						0x04
#define 	ENQ						0x05
#define 	ACK						0x06
#define 	DLE						0x10
#define 	NAK						0x15
#define 	SYN						0x16
#define 	ETB						0x17

#endif /* DEXCOMMANDS_H_ */
